import { isAbsolute, join, relative, resolve } from "node:path";

import type { FeatureId } from "../core";
import type { ProgressionUnlockSet } from "../progression";
import type { RuntimePaths } from "./types";

export type GateApprovalMode = "always-ask" | "write" | "yolo";

export type GateSurface =
  | { readonly kind: "tool"; readonly name: string }
  | { readonly kind: "skill"; readonly glob: string }
  | { readonly kind: "mcpServer"; readonly name: string }
  | { readonly kind: "extension"; readonly id: string }
  | { readonly kind: "provider"; readonly id: string }
  | { readonly kind: "approvalMode"; readonly mode: GateApprovalMode };

export type GateCatalog = Readonly<Record<string, readonly GateSurface[]>>;
export type GateCapability =
  | `tool:${string}`
  | `skill:${string}`
  | `mcp:${string}`
  | `extension:${string}`
  | `provider:${string}`
  | `approvalMode:${GateApprovalMode}`;

export type GateConfigData = Readonly<Record<string, unknown>>;

export interface GateMcpConfigData {
  readonly $schema: typeof mcpSchemaUrl;
  readonly garnish: GeneratedByGarnishMarker;
  readonly mcpServers: Readonly<Record<string, never>>;
  readonly disabledServers: readonly string[];
}

export interface RenderedGateConfig {
  readonly configPath: "config.yml";
  readonly mcpPath: "mcp.json";
  readonly config: GateConfigData;
  readonly mcp: GateMcpConfigData;
  readonly configYml: string;
  readonly mcpJson: string;
}

export interface GateConfigEffects {
  readonly mkdirp: (path: string) => void | Promise<void>;
  readonly writeFile: (path: string, content: string) => void | Promise<void>;
}

export interface GateParityIssue {
  readonly capability: GateCapability;
  readonly reason: string;
}

export interface GateParityComparison {
  readonly ok: boolean;
  readonly issues: readonly GateParityIssue[];
}

export interface GateMonotonicityViolation {
  readonly fromIndex: number;
  readonly toIndex: number;
  readonly capability: GateCapability;
}

interface GeneratedByGarnishMarker {
  readonly owner: "garnish";
  readonly generated: true;
  readonly arraysReplaceWholesale: readonly string[];
}

type CatalogSurfaceEntry = {
  readonly featureId: string;
  readonly surface: GateSurface;
  readonly capability: GateCapability;
};

type MutableRecord = Record<string, unknown>;

const mcpSchemaUrl = "https://raw.githubusercontent.com/can1357/oh-my-pi/main/packages/coding-agent/src/config/mcp-schema.json";
const generatedHeader = "# Generated by Garnish. Do not edit; Garnish owns these arrays and replaces them wholesale.\n";
const generatedMarker: GeneratedByGarnishMarker = {
  owner: "garnish",
  generated: true,
  arraysReplaceWholesale: [
    "disabledExtensions",
    "disabledProviders",
    "skills.includeSkills",
    "mcp.disabledServers",
  ],
};

